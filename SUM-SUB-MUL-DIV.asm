MOV BL,00
MOV AH,00 
MOV RES,00

MOV AL,NUM1
SUB AL,NUM2
MOV RES,AL

MOV AL,00 
MOV AH,00
MOV AL,RES
MOV BL,10
DIV BL
MOV REM,AH


MOV DL,AL
MOV AH,02H
ADD DL,48
INT 21H 

MOV DL,REM
MOV AH,02H
ADD DL,48
INT 21H

;MULTIPLICATION 

MULTIPLICATION:  

