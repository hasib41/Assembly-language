JE OPERATION  

SUB AL,48

MOV CL,AL
MOV AL,BL
MUL DL

ADD AL,CL  
MOV BL,AL
MOV NUM2,BL

JMP SCANNUM2

OPERATION: 

MOV DL,10 
MOV AH,02H
INT 21H
        

MOV DL,OFFSET MSG3
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H

SUB AL,48

CMP AL,1
JE SUM  

CMP AL,2
JE SUBTRACT 

CMP AL,3
JE MULTIPLICATION

CMP AL,4
JE DIVV

SUM:  

MOV DL,10 
MOV AH,02H
INT 21H
        

MOV DL,OFFSET MSG4
MOV AH,09H
INT 21H

MOV AL,00
MOV BL,00
MOV AH,00 
MOV RES,00

MOV AL,NUM1
ADD AL,NUM2
MOV RES,AL

MOV AL,00 
MOV AH,00
MOV AL,RES
MOV BL,10
DIV BL
MOV REM,AH


MOV DL,AL
MOV AH,02H
ADD DL,48
INT 21H 

MOV DL,REM
MOV AH,02H
ADD DL,48
INT 21H


;SUBTRACT
SUBTRACT:  

