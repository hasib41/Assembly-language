ADD DL,48
INT 21H 

MOV DL,REM
MOV AH,02H
ADD DL,48
INT 21H 


;DIVISION
DIVV:  

MOV DL,10 
MOV AH,02H
INT 21H
        

MOV DL,OFFSET MSG7
MOV AH,09H
INT 21H

MOV AL,00
MOV BL,00
MOV AH,00 
MOV RES,00

