.MODEL SMALL
.STACK 100H

.DATA

MSG1 DB "ENTER 1ST NUMBER :$" 
MSG2 DB "ENTER 2ND NUMBER :$"
MSG3 DB "ENTER 1 FOR ADD, 2 FOR SUBTRACT,3 FOR MUL AND 4 FOR DIV :$"
MSG4 DB "SUM IS :$" 
MSG5 DB "SUBTRACT IS :$" 
MSG6 DB "MULTIPLICATION IS :$" 
MSG7 DB "DIVISION IS :$"
NUM1 DB 00
NUM2 DB 00
RES DB ?
REM DB ?


.CODE  

MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV DL,OFFSET MSG1
MOV AH,09H
INT 21H
            
MOV AL,00
MOV BL,00
MOV DL,10
MOV AH,00

SCANNUM1:

MOV AH,01H
INT 21H

CMP AL,13
JE SECOND  

SUB AL,48

MOV CL,AL
MOV AL,BL
MUL DL

ADD AL,CL  
MOV BL,AL
MOV NUM1,BL

JMP SCANNUM1

SECOND: 

MOV DL,10 
MOV AH,02H
INT 21H
        

MOV DL,OFFSET MSG2
MOV AH,09H
INT 21H
            
MOV AL,00
MOV BL,00
MOV DL,10
MOV AH,00

SCANNUM2:

