     MODEL SMALL

.STACK 100H

.DATA

ARR DB 5 DUP(?) 
N DB ?

.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX  

;INPUT ARRAY

LEA SI,ARR
MOV CX,5

INPUT:

MOV AH,01H
INT 21H
SUB AL,48
MOV [SI],AL
INC SI


LOOP INPUT 


;INPUT2 

MOV AH,01H
INT 21H
SUB AL,48
MOV N,AL


LEA SI,ARR
MOV CX,5

OUTPUT:

MOV AL,[SI]
CMP AL,N

JE EQUAL 

MOV DL,[SI]
ADD DL,48
MOV AH,02H
INT 21H  



EQUAL:

INC SI




LOOP OUTPUT  

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN