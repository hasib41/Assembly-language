.MODEL SMALL
.STACK 100H

.DATA

MSG1 DB "A IS GREATER $"  
MSG2 DB "B IS GREATER $"
MSG3 DB "C IS GREATER $"

A DB 00
B DB 00
C DB 00

.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV AH,01H
INT 21H

MOV A,AL
MOV AL,00
MOV AH,00 

MOV DL,10
MOV AH,02H
INT 21H  

MOV DL,13
MOV AH,02H
INT 21H 


MOV AH,01H
INT 21H

MOV B,AL
MOV AL,00
MOV AH,00 

MOV DL,10
MOV AH,02H
INT 21H
        
MOV DL,13
MOV AH,02H
INT 21H 

MOV AH,01H
INT 21H

MOV C,AL
MOV AL,00
MOV AH,00 

MOV DL,10
MOV AH,02H
INT 21H 

MOV DL,13
MOV AH,02H
INT 21H 

MOV AL,00
MOV AH,00
MOV BL,00
MOV CL,00

MOV AL,A
MOV BL,B
MOV CL,C

CMP AL,BL
JG AC
JL BC

AC:
CMP AL,CL
JG ABIGGG
JL CBIGGG

BC:
CMP BL,CL
JG BBIGGG
JL CBIGGG

ABIGGG:
      MOV DL,10
      MOV AH,02H
      INT 21H  
      
      MOV DL,OFFSET MSG1
      MOV AH,09
      INT 21H 
      
      JMP EXIT

BBIGGG: 

      MOV DL,10
      MOV AH,02H
      INT 21H  
      
      MOV DL,OFFSET MSG2
      MOV AH,09
      INT 21H 
      
      JMP EXIT

CBIGGG: 


      MOV DL,10
      MOV AH,02H
      INT 21H  
      
      MOV DL,OFFSET MSG3
      MOV AH,09
      INT 21H 
      
      
      
EXIT:

MOV AX,4C00H
INT 21H

MAIN ENDP
END MAIN